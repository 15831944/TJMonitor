        /// <summary>
        /// 获得数据列表
        /// </summary>
        public List<TJMonitor.Model.CameraSet> DataTableToList(DataTable dt)
        {
            List<TJMonitor.Model.CameraSet> modelList = new List<TJMonitor.Model.CameraSet>();
            int rowsCount = dt.Rows.Count;
            if (rowsCount > 0)
            {
                TJMonitor.Model.CameraSet model;
                for (int n = 0; n < rowsCount; n++)
                {
                    model = dal.DataRowToModel(dt.Rows[n]);
                    if (model != null)
                    {
                        modelList.Add(model);
                    }
                }
            }
            return modelList;
        }

